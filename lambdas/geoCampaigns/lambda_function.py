def h_import(req):
    ids = [str(x) for x in (req.get("projectIds") or [])]
    dry = bool(req.get("dryRun"))
    projs = {str(p.get("id")): p for p in fetch_workduo_projects()}
    imported, diagnostics = [], {}
    targets = ids or list(projs.keys())[: int(req.get("limit", 5))]
    for pid in targets:
        project = projs.get(pid)
        if not project:
            diagnostics[pid] = "project_not_found"
            continue
        definition, diag = extract_definition(project)
        diagnostics[pid] = diag
        if not dry:
            item = dict(definition)
            item["createdAt"] = _now()
            item["updatedAt"] = _now()
            item["hasMetrics"] = False
            # don't clobber existing metrics on re-import
            existing = _table.get_item(Key={"id": pid}).get("Item")
            if existing and existing.get("metrics"):
                item["metrics"] = existing["metrics"]
                item["hasMetrics"] = True
                item["createdAt"] = existing.get("createdAt", item["createdAt"])
            # Merge topics so any manual per-query re-tagging done in the UI survives a
            # re-import: fresh Workduo topics are the base, existing overrides win on top.
            if existing and isinstance(existing.get("topics"), dict):
                merged = dict(item.get("topics") or {})
                merged.update(existing["topics"])
                item["topics"] = merged
            _table.put_item(Item=item)
        imported.append(definition)
    return _resp(200, {"imported": len(imported), "dryRun": dry,
                       "campaigns": imported, "diagnostics": diagnostics})


def h_list():
    items, kwargs = [], {}
    while True:
        page = _table.scan(**kwargs)
        items.extend(page.get("Items", []))
        if "LastEvaluatedKey" not in page:
            break
        kwargs["ExclusiveStartKey"] = page["LastEvaluatedKey"]
    out = []
    for it in items:
        iid = str(it.get("id") or "")
        if iid.startswith(RUN_PREFIX) or "#m#" in iid or it.get("type") == "metrics_month":
            continue  # live-run jobs + per-month snapshots share this table — never list them as campaigns
        self_vis, days = None, 0
        if it.get("metrics"):
            try:
                mm = json.loads(it["metrics"])
                self_vis = (mm.get("self") or {}).get("visibility")
                days = len(mm.get("visibilitySeries") or [])
            except Exception:
                pass
        out.append({
            "id": it.get("id"), "name": it.get("name"), "brand": it.get("brand"),
            "target": it.get("target"), "domain": it.get("domain"), "region": it.get("region"),
            "competitors": it.get("competitors", []), "prompts": it.get("prompts", []),
            "alternativeNames": it.get("alternativeNames", []),
            "hasMetrics": bool(it.get("metrics")), "selfVisibility": self_vis, "dataDays": days,
            "metricsSource": it.get("metricsSource"),
            "monthsAvailable": [str(x) for x in (it.get("monthsAvailable") or [])],
            "latestMonth": it.get("latestMonth"),
            "monthlyPlatforms": [str(x) for x in (it.get("monthlyPlatforms") or [])],
            "monthlyEnabled": it.get("monthlyEnabled"),
            "monthlyIncludeCompetitors": bool(it.get("monthlyIncludeCompetitors")),
            "updatedAt": it.get("updatedAt"), "metricsUpdatedAt": it.get("metricsUpdatedAt"),
            "activeRunId": it.get("activeRunId"),  # non-null while a live run is in progress for this campaign
        })
    out.sort(key=lambda c: (c.get("name") or "").lower())
    return _resp(200, {"count": len(out), "campaigns": out})


def h_get(req):
    cid = str(req.get("id") or "")
    it = _table.get_item(Key={"id": cid}).get("Item")
    if not it:
        return _resp(404, {"error": "not found"})
    if it.get("metrics"):
        try:
            it["metrics"] = json.loads(it["metrics"])
        except Exception:
            pass
    if isinstance(it.get("history"), str):
        try:
            it["history"] = json.loads(it["history"])
        except Exception:
            pass
    return _resp(200, {"campaign": it})


def _month_item_id(cid, month):
    # Per-month full-model snapshots live in this table under a derived id so any number
    # of months can be archived without ever bumping the campaign record toward the 400KB cap.
    return cid + "#m#" + month


def _history_point(month, model):
    """A tiny per-month summary for the month-over-month trend (kept on the campaign record)."""
    self_m = (model.get("self") or {}) if isinstance(model, dict) else {}
    def _n(v):
        try:
            return round(float(v), 2)
        except Exception:
            return 0
    return {"month": month,
            "positive": int(model.get("positive", 0) or 0) if isinstance(model, dict) else 0,
            "negative": int(model.get("negative", 0) or 0) if isinstance(model, dict) else 0,
            "visibility": _n(self_m.get("visibility")), "sov": _n(self_m.get("sov")),
            "mentions": int(self_m.get("mentions", 0) or 0),
            "ownedShare": _n(model.get("ownedShare") if isinstance(model, dict) else 0)}


def _answers_key(cid, month, run_id):
    return f"answers/{cid}/{month}/{run_id}.json"


def _write_answers(cid, month, run_id, cfg, results):
    """Persist every AI answer from a run to S3 — including cells where the brand was NOT
    mentioned and competitor cells, both of which used to be discarded. The absent case is the
    most actionable one ("who did it recommend instead of us?"), so it must be kept.

    Returns the S3 key, or None if the write failed. A failure is non-fatal: the run still
    archives, just without browsable answers, rather than losing the whole month's metrics."""
    prompts = cfg.get("prompts") or []
    entities = cfg.get("entities") or []
    cells = []
    for o in results:
        r = o.get("res") or {}
        cells.append({
            "ei": o.get("ei"), "pi": o.get("pi"), "pl": o.get("pl"),
            "engine": _plat_label(o.get("pl")),
            "entity": (entities[o["ei"]].get("name") if isinstance(o.get("ei"), int)
                       and 0 <= o["ei"] < len(entities) and isinstance(entities[o["ei"]], dict) else ""),
            "prompt": o.get("prompt") or "",
            "ok": bool(r.get("ok")), "error": r.get("error") or "",
            "mentioned": bool(r.get("mentioned")), "sentiment": r.get("sentiment") or "",
            "snippet": r.get("snippet") or "", "reason": r.get("reason") or "",
            "theme": r.get("theme") or "", "score": r.get("score") or 0,
            "rank": r.get("rank") or 0, "cited": bool(r.get("cited")),
            "citations": r.get("citations") or [],
            "response": r.get("response") or "",
        })
    blob = {"campaignId": cid, "month": month, "runId": run_id, "createdAt": _now(),
            "brand": cfg.get("brand") or "", "target": cfg.get("target") or "",
            "entities": [e.get("name") if isinstance(e, dict) else str(e) for e in entities],
            "prompts": prompts, "cells": cells}
    key = _answers_key(cid, month, run_id)
    try:
        _s3.put_object(Bucket=ANSWERS_BUCKET, Key=key,
                       Body=json.dumps(blob).encode("utf-8"),
                       ContentType="application/json")
        return key
    except Exception as e:
        print(f"[GEO] answer-store write failed for {cid}/{month}: {e}")
        return None


def _merge_answers(cid, month, run_id, cfg, results, prompt_index):
    """Fold a single-prompt run's answers into the month's existing answer blob.

    Read-modify-write: cells for this prompt are replaced, every other prompt is left alone. If the
    month has no blob yet (or it can't be read), this writes a fresh one containing just this
    prompt — better than nothing, and the next full run supersedes it.

    Returns the S3 key, or None if the write failed (non-fatal — the answers still come back to the
    browser through run_status, so a re-run is useful even when persistence is unavailable)."""
    existing = None
    it = _table.get_item(Key={"id": _month_item_id(cid, month)}).get("Item") or {}
    old_key = it.get("answersKey")
    if old_key:
        try:
            existing = _read_answers(old_key)
        except Exception as e:
            print(f"[GEO] merge: could not read existing answers {old_key}: {e}")
    # Reuse _write_answers' cell shape by building the new blob through it, then splicing.
    fresh_key = _write_answers(cid, month, run_id, cfg, results)
    if not fresh_key:
        return None
    try:
        fresh = _read_answers(fresh_key)
    except Exception:
        return fresh_key
    if not existing:
        return fresh_key
    kept = [c for c in (existing.get("cells") or []) if c.get("pi") != prompt_index]
    existing["cells"] = kept + (fresh.get("cells") or [])
    existing["updatedAt"] = _now()
    try:
        _s3.put_object(Bucket=ANSWERS_BUCKET, Key=old_key,
                       Body=json.dumps(existing).encode("utf-8"),
                       ContentType="application/json")
        return old_key
    except Exception as e:
        print(f"[GEO] merge: write-back to {old_key} failed: {e}")
        return fresh_key


def _read_answers(key):
    obj = _s3.get_object(Bucket=ANSWERS_BUCKET, Key=key)
    return json.loads(obj["Body"].read().decode("utf-8"))


def h_get_answers(req):
    """Fetch stored AI answers. Without pi/ei this returns an INDEX (no answer text) so the UI can
    render the list cheaply; with pi (and optionally ei) it returns those cells' full text. That
    keeps the dashboard's normal load free of megabyte payloads — text arrives only on expand."""
    cid = str(req.get("id") or "")
    month = str(req.get("month") or "").strip()
    if not cid or not month:
        return _resp(400, {"error": "id and month required"})
    key = req.get("key")
    if not key:
        it = _table.get_item(Key={"id": _month_item_id(cid, month)}).get("Item") or {}
        key = it.get("answersKey")
        if not key:
            return _resp(404, {"error": "no stored answers for that month — re-run the campaign"})
    try:
        blob = _read_answers(key)
    except Exception as e:
        # Never surface the raw boto error: S3 answers a missing key with AccessDenied (not
        # NoSuchKey) unless the role also holds s3:ListBucket, and that message embeds the account
        # id and role ARN. Log the detail, hand the browser a plain sentence.
        print(f"[GEO] answer read failed for {key}: {e}")
        return _resp(404, {"error": "no stored answers for that month — re-run the campaign"})

    cells = blob.get("cells") or []
    pi, ei = req.get("pi"), req.get("ei")
    if pi is None and ei is None:
        idx = [{k: c.get(k) for k in ("ei", "pi", "pl", "engine", "entity", "prompt", "ok",
                                      "mentioned", "sentiment", "theme", "score", "rank", "cited")}
               for c in cells]
        for c, i in zip(cells, idx):
            i["chars"] = len(c.get("response") or "")
        return _resp(200, {"month": blob.get("month"), "prompts": blob.get("prompts") or [],
                           "entities": blob.get("entities") or [], "index": idx,
                           "createdAt": blob.get("createdAt")})
    sel = [c for c in cells
           if (pi is None or c.get("pi") == pi) and (ei is None or c.get("ei") == ei)]
    return _resp(200, {"month": blob.get("month"), "cells": sel})


MAX_ITEM_BYTES = 300000  # headroom under DynamoDB's 400KB hard cap


def _shrink_model(model):
    """Bound an archived model to DynamoDB's item cap, shedding the most redundant data first.

    Answer text now lives in S3, so a normal model is small and none of this fires. It remains a
    backstop for legacy/oversized models: an unbounded put_item here would throw, and because
    autoArchive runs unattended that surfaced as a silently missing month rather than an error.
    Order matters — theme examples are duplicates of sentimentEvidence entries, so they go first."""
    if len(json.dumps(model)) <= MAX_ITEM_BYTES:
        return model, None
    dropped = []
    for t in (model.get("sentimentThemes") or []):
        for e in (t.get("examples") or []):
            e.pop("response", None)
    for e in (model.get("sentimentEvidence") or []):
        e.pop("response", None)
    dropped.append("inline answer text (still in the answer store)")
    if len(json.dumps(model)) > MAX_ITEM_BYTES:
        for t in (model.get("sentimentThemes") or []):
            t["examples"] = (t.get("examples") or [])[:2]
        dropped.append("theme examples trimmed to 2")
    if len(json.dumps(model)) > MAX_ITEM_BYTES:
        model["sentimentEvidence"] = (model.get("sentimentEvidence") or [])[:80]
        dropped.append("evidence capped at 80")
    if len(json.dumps(model)) > MAX_ITEM_BYTES:
        model["citations"] = (model.get("citations") or [])[:200]
        dropped.append("citations capped at 200")
    note = "; ".join(dropped)
    model["_trimmed"] = note
    print(f"[GEO] model exceeded {MAX_ITEM_BYTES}B — trimmed: {note}")
    return model, note


def _archive_model(cid, month, model, answers_key=None):
    """Persist a model permanently: full month snapshot as its own item + merge the month
    into the campaign record's index (monthsAvailable) and compact month-over-month history.
    Shared by the API save_metrics path AND the headless run worker (so cron runs archive
    with no browser open). Returns the updated monthsAvailable list."""
    model, _trim_note = _shrink_model(model)
    model_json = json.dumps(model)
    now = _now()
    item = {"id": _month_item_id(cid, month), "type": "metrics_month",
            "campaignId": cid, "month": month, "model": model_json, "updatedAt": now}
    # Pointer to this month's full answers in S3; the UI lazy-loads text through get_answers.
    if not answers_key:
        prev = _table.get_item(Key={"id": _month_item_id(cid, month)}).get("Item") or {}
        answers_key = prev.get("answersKey")
    if answers_key:
        item["answersKey"] = answers_key
    _table.put_item(Item=item)
    it = _table.get_item(Key={"id": cid}).get("Item") or {}
    months = [str(x) for x in (it.get("monthsAvailable") or [])]
    if month not in months:
        months.append(month)
    months = sorted(set(months))
    try:
        hist = json.loads(it["history"]) if isinstance(it.get("history"), str) else (it.get("history") or [])
    except Exception:
        hist = []
    hist = [h for h in hist if h.get("month") != month] + [_history_point(month, model)]
    hist = sorted(hist, key=lambda h: h.get("month") or "")
    _table.update_item(
        Key={"id": cid},
        UpdateExpression="SET #m = :m, hasMetrics = :h, metricsUpdatedAt = :t, updatedAt = :t, "
                         "monthsAvailable = :ma, history = :hi, latestMonth = :lm",
        ExpressionAttributeNames={"#m": "metrics"},
        ExpressionAttributeValues={":m": model_json, ":h": True, ":t": now, ":ma": months,
                                   ":hi": json.dumps(hist), ":lm": month},
    )
    return months


