PALETTE = ['#6b5ce7', '#14b8a6', '#f59e0b', '#ec4899', '#fda4af', '#38bdf8', '#a78bfa']
METRICS_DATE_RANGE = os.environ.get("WORKDUO_METRICS_RANGE", "last30days")


def _range_spec(req):
    """Resolve a date-range request into (query_string, preset, display_label).
    Workduo supports only last7days, last30days, or custom startDate/endDate."""
    dr = (req.get("dateRange") or "last30days").strip()
    if dr == "custom":
        sd, ed = req.get("startDate"), req.get("endDate")
        if not (sd and ed):
            raise RuntimeError("custom range requires startDate and endDate (YYYY-MM-DD)")
        return f"dateRange=custom&startDate={sd}&endDate={ed}", dr, f"{sd} → {ed}"
    if dr not in ("last7days", "last30days"):
        raise RuntimeError("unsupported dateRange — use last7days, last30days, or custom")
    return f"dateRange={dr}", dr, dr


def _wd_get_metrics(eid, pid, dimension, interval="daily", range_qs=None):
    """Paginated metrics rows for one entity/dimension over a window.
    interval='daily' for time-series, 'selectedDateRange' for one aggregate row each."""
    rows, token, pages, page_size = [], None, 0, 100
    rng = range_qs or f"dateRange={METRICS_DATE_RANGE}"
    base = (f"{WD_DATA}/metrics/entities/{eid}?projectId={pid}"
            f"&{rng}&dimension={dimension}&interval={interval}")
    while pages < 80:
        u = base + (f"&pageSize={page_size}" if page_size else "")
        if token:
            u += "&nextPageToken=" + token
        st, b = _wd_get(u)
        if st != 200:
            if page_size:          # some windows reject pageSize — retry without it
                page_size = 0
                continue
            break
        rows.extend(_rows(b))
        token = b.get("nextPageToken") if isinstance(b, dict) else None
        pages += 1
        if not token:
            break
    return rows


def _wd_get_citations(eid, pid, dimension, range_qs=None, on_page=None, deadline=None, max_pages=1000):
    """Paginated rows from Workduo's /core/v1/citations — a separate endpoint from
    /data/v1/metrics, with dimension='url'|'domain'|'query'. One row per
    dimensionValue per day; caller aggregates across the requested range.
    Cursor-only pagination (no offset/page param — verified it's silently
    ignored), so pages are inherently sequential; ~4-9s/call regardless of
    pageSize (observed variance), so 500 (the largest accepted) minimizes round
    trips. Bounded by wall-clock `deadline` (a time.time() cutoff), NOT a fixed
    page count — a fixed cap silently truncated real campaigns (a pageSize=200
    run and a pageSize=500 run each independently maxed out at exactly 80 pages
    — 16000 and 40000 rows respectively — proof the true total was larger and
    got cut off both times). `max_pages` is just a runaway backstop, set high
    enough to never bind before the deadline does for any dataset seen so far.
    on_page(pages, rows_so_far), if given, fires after every page — the async
    worker uses this to persist live progress for the frontend to poll."""
    rows, token, pages, page_size = [], None, 0, 500
    rng = range_qs or f"dateRange={METRICS_DATE_RANGE}"
    base = (f"{WD_CORE}/citations?projectId={pid}&entityId={eid}"
            f"&{rng}&dimension={dimension}")
    while pages < max_pages:
        if deadline and time.time() >= deadline:
            break
        u = base + (f"&pageSize={page_size}" if page_size else "")
        if token:
            u += "&nextPageToken=" + token
        st, b = _wd_get(u)
        if st != 200:
            if page_size:          # some windows reject pageSize — retry without it
                page_size = 0
                continue
            break
        rows.extend(_rows(b))
        token = b.get("nextPageToken") if isinstance(b, dict) else None
        pages += 1
        if on_page:
            try:
                on_page(pages, len(rows))
            except Exception:
                pass
        if not token:
            return rows, False   # exhausted — complete
    return rows, True            # hit the deadline/max_pages backstop — truncated


_CIT_TYPE_LABEL = {"OWNED": "Owned", "COMPETITOR": "Competitor", "OTHERS": "Other"}
_CIT_TYPE_COLOR = {"Owned": "#1e3a8a", "Competitor": "#f59e0b", "Social": "#14b8a6", "Other": "#8b5cf6"}


def _citations_model(eid, pid, range_qs=None, on_page=None, deadline=None):
    """Per-URL citation detail for one entity, built from /core/v1/citations
    (dimension=url). Returns (citations, citationTypes, available, truncated).
    Workduo gives real type (OWNED/COMPETITOR/OTHERS) and used-count per URL per
    day — this aggregates those across the range into the shape the dashboard
    table expects. `truncated=True` means pagination didn't run to exhaustion
    (hit the deadline or an API error) — the totals below are real but partial.
    Note: Workduo doesn't expose a per-URL x per-query cross-tab, so `queryCount`
    is the citation's real times-used count (citationUsedCount), not a distinct-
    query count — the closest genuine signal Workduo offers for that column."""
    try:
        rows, truncated = _wd_get_citations(eid, pid, "url", range_qs=range_qs, on_page=on_page, deadline=deadline)
    except Exception:
        return [], [], False, True
    agg = {}
    for r in rows:
        url = r.get("dimensionValue")
        if not url:
            continue
        a = agg.setdefault(url, {"count": 0, "usedCount": 0, "responses": 0, "type": r.get("type") or "OTHERS"})
        a["count"] += r.get("citationCount", 0) or 0
        a["usedCount"] += r.get("citationUsedCount", 0) or 0
        a["responses"] += r.get("responseCount", 0) or 0
        if r.get("type"):
            a["type"] = r["type"]
    total = sum(a["count"] for a in agg.values()) or 1
    cits = []
    for url, a in agg.items():
        label = _CIT_TYPE_LABEL.get(a["type"], "Other")
        cits.append({
            "url": url, "domain": domain_of(url), "count": a["count"],
            "queryCount": a["usedCount"],
            "used": round(a["usedCount"] / a["responses"] * 100, 1) if a["responses"] else 0,
            "share": round(a["count"] / total * 100, 1),
            "owned": label == "Owned", "type": label,
        })
    cits.sort(key=lambda c: -c["count"])
    type_totals = {"Owned": 0, "Competitor": 0, "Social": 0, "Other": 0}
    for c in cits:
        type_totals[c["type"]] = type_totals.get(c["type"], 0) + c["count"]
    citation_types = [{"type": t, "count": n, "color": _CIT_TYPE_COLOR[t]}
                       for t, n in type_totals.items() if n > 0]
    return cits, citation_types, True, truncated


def _agg_overall(rows):
    m = sum(r.get("mentions", 0) or 0 for r in rows)
    resp = sum(r.get("totalResponses", 0) or 0 for r in rows)
    ent = sum(r.get("totalEntitiesMentions", 0) or 0 for r in rows)
    pos = [r["position"] for r in rows if r.get("position") and r.get("mentions")]
    occ = sum(r.get("ownedCitationCount", 0) or 0 for r in rows)
    ocs = [r.get("ownedCitationShare") for r in rows if r.get("ownedCitationShare") is not None]
    return {
        "visibility": round(m / resp * 100, 1) if resp else 0,
        "sov": round(m / ent * 100, 1) if ent else 0,
        "avgPosition": round(sum(pos) / len(pos), 1) if pos else 0,
        "mentions": m, "total": resp,
        "ownedCount": occ,
        "ownedShare": round(sum(ocs) / len(ocs) * 100, 1) if ocs else 0,
    }


def _daily(rows):
    by = {}
    for r in rows:
        d = (r.get("date") or "")[:10]
        if not d:
            continue
        x = by.setdefault(d, {"m": 0, "r": 0})
        x["m"] += r.get("mentions", 0) or 0
        x["r"] += r.get("totalResponses", 0) or 0
    return {d: round(x["m"] / x["r"] * 100, 1) if x["r"] else 0 for d, x in by.items()}


def _daily_by_topic(rows):
    """Per-topic daily visibility {date: {topic: vis%}} from topic-dimension daily rows.
    (Workduo returns one row per topic per day; _daily collapses that, this keeps it.)"""
    by = {}
    for r in rows:
        d = (r.get("date") or "")[:10]
        if not d:
            continue
        t = r.get("topic") or "General"
        x = by.setdefault(d, {}).setdefault(t, {"m": 0, "r": 0})
        x["m"] += r.get("mentions", 0) or 0
        x["r"] += r.get("totalResponses", 0) or 0
    return {d: {t: round(v["m"] / v["r"] * 100, 1) if v["r"] else 0 for t, v in tv.items()}
            for d, tv in by.items()}


def build_workduo_model(campaign, proj=None, range_qs=None, range_label=None, fetch_citations=False):
    """Transform real Workduo metrics into the dashboard's WD.model shape.
    Per-URL citation detail comes from the separate /core/v1/citations endpoint
    (see _citations_model) — OFF by default. Its pagination is cursor-only
    (~7-9s per call, un-parallelizable) and a full 30-day pull can take 3-4
    minutes for a campaign with heavy citation volume, blowing API Gateway's
    hard 30s Lambda-proxy timeout. Callers reached via API Gateway (h_import_metrics,
    h_migrate_all) must NOT set this; only an async/self-invoked path should.
    Sentiment has no Workduo equivalent at all, so that stays flagged unavailable
    rather than fabricated."""
    pid = str(campaign["id"])
    if proj is None:
        projects = {str(p.get("id")): p for p in fetch_workduo_projects()}
        proj = projects.get(pid) or {}
    self_eid = (proj.get("entity") or {}).get("id")
    brand = campaign.get("brand") or (proj.get("entity") or {}).get("name") or campaign.get("name")
    target = campaign.get("target") or (proj.get("entity") or {}).get("url") or ""
    domain = campaign.get("domain") or domain_of(target)
    region = campaign.get("region") or "Singapore"

    # entities (self + competitors)
    ents_raw = _rows(_wd_get(f"{WD_CORE}/entities", {"projectId": pid, "pageSize": 100})[1])
    metas = []
    for e in ents_raw:
        if not isinstance(e, dict) or not e.get("id"):
            continue
        metas.append({"id": e["id"], "name": e.get("name") or "",
                      "url": e.get("url") or "", "isSelf": e.get("id") == self_eid})
    # self first, then competitors; cap to keep within timeout
    metas.sort(key=lambda m: 0 if m["isSelf"] else 1)
    metas = metas[:6]

    # queryId -> text/topic
    qmap = {}
    for q in _rows(_wd_get(f"{WD_CORE}/queries", {"projectId": pid, "pageSize": 200})[1]):
        if isinstance(q, dict) and q.get("id"):
            qmap[q["id"]] = {"text": q.get("query") or q["id"],
                             "topic": q.get("topic") or "General",
                             "region": q.get("region") or region}

    ent_models, series_by_date = [], {}
    self_qrows, primary_qrows, primary_name = None, None, None
    self_topic_daily = {}
    for em in metas:
        trows = _wd_get_metrics(em["id"], pid, "topic", range_qs=range_qs)
        agg = _agg_overall(trows)
        nm = em["name"] or em["id"]
        ent_models.append({
            "name": nm, "domain": domain_of(em["url"]) or (domain if em["isSelf"] else ""),
            "isSelf": em["isSelf"], **agg,
        })
        for d, v in _daily(trows).items():
            series_by_date.setdefault(d, {})[nm] = v
        if em["isSelf"]:
            # keep the per-topic daily split for the tracked brand (powers the chart's topic filter)
            self_topic_daily = _daily_by_topic(trows)
            # one aggregate row per query (interval=selectedDateRange) — fast, no per-day pages
            self_qrows = _wd_get_metrics(em["id"], pid, "query", "selectedDateRange", range_qs=range_qs)
        elif primary_qrows is None:
            primary_qrows = _wd_get_metrics(em["id"], pid, "query", "selectedDateRange", range_qs=range_qs)
            primary_name = nm

    ent_models.sort(key=lambda e: -e["visibility"])
    self_m = next((e for e in ent_models if e["isSelf"]), ent_models[0] if ent_models else None)
    if not self_m:
        raise RuntimeError("no entity metrics returned from Workduo")

    # SOV breakdown (top 5 by sov + Others)
    by_sov = sorted(ent_models, key=lambda e: -e["sov"])
    sov_break, used = [], 0
    for i, e in enumerate(by_sov[:5]):
        sov_break.append({"name": e["name"], "value": e["sov"],
                          "color": PALETTE[i % len(PALETTE)], "isSelf": e["isSelf"]})
        used += e["sov"]
    rest = round(max(0, 100 - used), 1)
    if rest > 0.5:
        sov_break.append({"name": "Others", "value": rest, "color": "#cbd5e1", "isSelf": False})

    # visibility-over-time by brand (+ per-topic split for the tracked brand)
    vis_series = []
    for d in sorted(series_by_date.keys()):
        bb = series_by_date[d]
        vis_series.append({"date": d, "self": bb.get(self_m["name"], 0), "sov": self_m["sov"],
                           "positive": 0, "negative": 0, "byBrand": bb,
                           "byTopic": self_topic_daily.get(d, {})})

    # per-query breakdown (self vs primary competitor)
    comp_by_q = {}
    for r in (primary_qrows or []):
        qid = r.get("queryId")
        c = comp_by_q.setdefault(qid, {"m": 0, "r": 0})
        c["m"] += r.get("mentions", 0) or 0
        c["r"] += r.get("totalResponses", 0) or 0
    prompts = []
    byq = {}
    for r in (self_qrows or []):
        qid = r.get("queryId")
        x = byq.setdefault(qid, {"m": 0, "r": 0, "pos": []})
        x["m"] += r.get("mentions", 0) or 0
        x["r"] += r.get("totalResponses", 0) or 0
        if r.get("position") and r.get("mentions"):
            x["pos"].append(r["position"])
    for qid, x in byq.items():
        info = qmap.get(qid, {})
        selfpct = round(x["m"] / x["r"] * 100, 1) if x["r"] else 0
        c = comp_by_q.get(qid)
        comppct = round(c["m"] / c["r"] * 100, 1) if (c and c["r"]) else 0
        prompts.append({"text": info.get("text", qid), "topic": info.get("topic", "General"),
                        "region": info.get("region", region), "responses": x["r"],
                        "visibility": selfpct, "sov": self_m["sov"],
                        "avgPosition": round(sum(x["pos"]) / len(x["pos"]), 1) if x["pos"] else 0,
                        "selfPct": selfpct, "competitorPct": comppct})
    prompts.sort(key=lambda p: -p["visibility"])

    cits, citation_types, cits_ok, _cits_truncated = (_citations_model(self_eid, pid, range_qs=range_qs)
                                                        if fetch_citations else ([], [], False, False))

    return {
        "brand": brand, "target": target, "domain": domain, "location": region,
        "live": True, "metricsSource": "workduo", "engineLabel": "Workduo (historical)",
        "generatedAt": _now(),
        "self": self_m, "entities": ent_models, "sovBreakdown": sov_break,
        "visibilitySeries": vis_series,
        "sentimentSeries": [{"date": s["date"], "positive": 0, "negative": 0} for s in vis_series],
        "prompts": prompts, "primaryComp": primary_name,
        "citations": cits, "citationTypes": citation_types,
        "ownedShare": self_m["ownedShare"], "ownedCount": self_m["ownedCount"],
        "positive": 0, "negative": 0, "sentimentThemes": [],
        "sentimentAvailable": False, "citationsDetailAvailable": cits_ok,
        "plats": [{"label": "ChatGPT"}, {"label": "Google AI Overview"}],
        "dateRange": range_label or METRICS_DATE_RANGE,
    }


