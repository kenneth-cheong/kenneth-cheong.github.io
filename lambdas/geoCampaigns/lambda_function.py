WD_PUB = os.environ.get("WORKDUO_PUBLIC_KEY", "")
WD_SEC = os.environ.get("WORKDUO_SECRET_KEY", "")
TABLE_NAME = os.environ.get("CAMPAIGNS_TABLE", "geoCampaigns")
WD_CORE = "https://api.workduo.ai/core/v1"
WD_DATA = "https://api.workduo.ai/data/v1"
TIMEOUT = int(os.environ.get("HTTP_TIMEOUT", "30"))

CORS = {
    "Access-Control-Allow-Origin": "*",
    "Access-Control-Allow-Headers": "content-type",
    "Access-Control-Allow-Methods": "POST,OPTIONS",
}

