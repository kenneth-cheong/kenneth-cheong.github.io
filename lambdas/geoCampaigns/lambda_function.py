"""
geoCampaigns — shared, persistent store of GEO campaigns for the GEO Analytics Dashboard.

Purpose: REPLICATE Workduo campaigns without depending on Workduo at runtime. It imports
campaign DEFINITIONS from Workduo once (brand, domain, competitors, tracked prompts, region),
stores them in DynamoDB, and serves them to every user of index.html. Metrics are NOT taken
from Workduo — they are regenerated by our own DataForSEO engine and saved back here, so the
dashboard's numbers are entirely our own data.

Actions (POST JSON {action, ...}):
  workduo_projects            -> list available Workduo projects (id, name, entity) to choose from
  import {projectIds, dryRun} -> pull definitions for those projects from Workduo, store in DynamoDB
  list                        -> all stored campaigns (definition + whether metrics exist)
  get {id}                    -> one campaign incl. saved metrics
  save_metrics {id, model}    -> persist a regenerated metrics snapshot (shared across users)
  delete {id}                 -> remove a campaign

Pure stdlib + boto3 (bundled in the Lambda runtime). Env: WORKDUO_PUBLIC_KEY, WORKDUO_SECRET_KEY,
CAMPAIGNS_TABLE (default 'geoCampaigns').
"""

