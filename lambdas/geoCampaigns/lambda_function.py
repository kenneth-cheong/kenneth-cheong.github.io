def lambda_handler(event, context):
    method = (event.get("requestContext", {}).get("http", {}).get("method")
              or event.get("httpMethod") or "POST")
    if method == "OPTIONS":
        return _resp(200, {"ok": True})

    raw = event.get("body")
    if raw is None and ("action" in event or "id" in event):
        req = event  # direct invoke convenience
    else:
        if event.get("isBase64Encoded") and isinstance(raw, str):
            raw = base64.b64decode(raw).decode()
        try:
            req = json.loads(raw) if isinstance(raw, str) else (raw or {})
        except Exception:
            req = {}

    action = req.get("action", "list")
    try:
        if action == "workduo_projects":
            return h_workduo_projects()
        if action == "import":
            return h_import(req)
        if action == "list":
            return h_list()
        if action == "get":
            return h_get(req)
        if action == "save_metrics":
            return h_save_metrics(req)
        if action == "delete":
            return h_delete(req)
        if action == "inspect":
            return h_inspect(req)
        if action == "inspect_metrics":
            return h_inspect_metrics(req)
        if action == "inspect_ranges":
            return h_inspect_ranges(req)
        if action == "import_metrics":
            return h_import_metrics(req)
        if action == "migrate_all":
            return h_migrate_all(req)
        if action == "metrics_for_range":
            return h_metrics_for_range(req)
        if action == "export_daily":
            return h_export_daily(req)
        if action == "export_daily_all":
            return h_export_daily_all(req)
        return _resp(400, {"error": f"unknown action: {action}"})
    except Exception as e:
        return _resp(500, {"error": str(e)})
