def _now():
    return time.strftime("%Y-%m-%dT%H:%M:%SZ", time.gmtime())


def h_workduo_projects():
    projs = fetch_workduo_projects()
    light = [{
        "id": str(p.get("id")),
        "name": _first(p, "name", default=str(p.get("id"))),
        "domain": domain_of(_first(p.get("entity") or {}, "url", "domain", default="")),
        "hasEntity": bool((p.get("entity") or {}).get("id")),
    } for p in projs]
    return _resp(200, {"count": len(light), "projects": light})


def h_import(req):
    ids = [str(x) for x in (req.get("projectIds") or [])]
    dry = bool(req.get("dryRun"))
    projs = {str(p.get("id")): p for p in fetch_workduo_projects()}
    imported, diagnostics = [], {}
    targets = ids or list(projs.keys())[: int(req.get("limit", 5))]
    for pid in targets:
        project = projs.get(pid)
        if not project:
            diagnostics[pid] = "project_not_found"
            continue
        definition, diag = extract_definition(project)
        diagnostics[pid] = diag
        if not dry:
            item = dict(definition)
            item["createdAt"] = _now()
            item["updatedAt"] = _now()
            item["hasMetrics"] = False
            # don't clobber existing metrics on re-import
            existing = _table.get_item(Key={"id": pid}).get("Item")
            if existing and existing.get("metrics"):
                item["metrics"] = existing["metrics"]
                item["hasMetrics"] = True
                item["createdAt"] = existing.get("createdAt", item["createdAt"])
            _table.put_item(Item=item)
        imported.append(definition)
    return _resp(200, {"imported": len(imported), "dryRun": dry,
                       "campaigns": imported, "diagnostics": diagnostics})


def h_list():
    items, kwargs = [], {}
    while True:
        page = _table.scan(**kwargs)
        items.extend(page.get("Items", []))
        if "LastEvaluatedKey" not in page:
            break
        kwargs["ExclusiveStartKey"] = page["LastEvaluatedKey"]
    out = []
    for it in items:
        if str(it.get("id") or "").startswith(RUN_PREFIX):
            continue  # live-run job items live in this table too — never list them as campaigns
        self_vis, days = None, 0
        if it.get("metrics"):
            try:
                mm = json.loads(it["metrics"])
                self_vis = (mm.get("self") or {}).get("visibility")
                days = len(mm.get("visibilitySeries") or [])
            except Exception:
                pass
        out.append({
            "id": it.get("id"), "name": it.get("name"), "brand": it.get("brand"),
            "target": it.get("target"), "domain": it.get("domain"), "region": it.get("region"),
            "competitors": it.get("competitors", []), "prompts": it.get("prompts", []),
            "alternativeNames": it.get("alternativeNames", []),
            "hasMetrics": bool(it.get("metrics")), "selfVisibility": self_vis, "dataDays": days,
            "metricsSource": it.get("metricsSource"),
            "updatedAt": it.get("updatedAt"), "metricsUpdatedAt": it.get("metricsUpdatedAt"),
            "activeRunId": it.get("activeRunId"),  # non-null while a live run is in progress for this campaign
        })
    out.sort(key=lambda c: (c.get("name") or "").lower())
    return _resp(200, {"count": len(out), "campaigns": out})


def h_get(req):
    cid = str(req.get("id") or "")
    it = _table.get_item(Key={"id": cid}).get("Item")
    if not it:
        return _resp(404, {"error": "not found"})
    if it.get("metrics"):
        try:
            it["metrics"] = json.loads(it["metrics"])
        except Exception:
            pass
    return _resp(200, {"campaign": it})


def h_save_metrics(req):
    cid = str(req.get("id") or "")
    model = req.get("model")
    if not cid or model is None:
        return _resp(400, {"error": "id and model required"})
    it = _table.get_item(Key={"id": cid}).get("Item")
    if not it:
        return _resp(404, {"error": "campaign not found — import it first"})
    _table.update_item(
        Key={"id": cid},
        UpdateExpression="SET #m = :m, hasMetrics = :h, metricsUpdatedAt = :t, updatedAt = :t",
        ExpressionAttributeNames={"#m": "metrics"},
        ExpressionAttributeValues={":m": json.dumps(model), ":h": True, ":t": _now()},
    )
    return _resp(200, {"ok": True, "id": cid, "metricsUpdatedAt": _now()})


def h_delete(req):
    cid = str(req.get("id") or "")
    _table.delete_item(Key={"id": cid})
    return _resp(200, {"ok": True, "deleted": cid})


