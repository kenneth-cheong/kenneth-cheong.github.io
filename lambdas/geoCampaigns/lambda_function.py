def _resp(status, body):
    return {"statusCode": status, "headers": {"Content-Type": "application/json", **CORS},
            "body": json.dumps(body, default=str)}


def _wd_get(url, params=None):
    if params:
        from urllib.parse import urlencode
        url = url + ("&" if "?" in url else "?") + urlencode(params)
    req = urllib.request.Request(url, method="GET")
    req.add_header("Authorization", "Basic " + base64.b64encode(f"{WD_PUB}:{WD_SEC}".encode()).decode())
    req.add_header("Accept", "application/json")
    try:
        with urllib.request.urlopen(req, timeout=TIMEOUT) as r:
            return r.status, json.loads(r.read().decode())
    except urllib.error.HTTPError as e:
        try:
            return e.code, json.loads(e.read().decode())
        except Exception:
            return e.code, {"error": f"HTTP {e.code}"}
    except Exception as e:
        return 0, {"error": str(e)}


def domain_of(u):
    if not u:
        return ""
    u = re.sub(r"^https?://", "", str(u)).split("/")[0]
    return re.sub(r"^www\.", "", u).lower()


def _rows(payload):
    if isinstance(payload, list):
        return payload
    if isinstance(payload, dict):
        return payload.get("data") or payload.get("results") or payload.get("items") or []
    return []


def fetch_workduo_projects():
    """Returns list of {id, name, entity:{...}} across all pages."""
    out, page = [], 1
    while True:
        status, body = _wd_get(f"{WD_CORE}/projects", {"page": page, "pageSize": 100})
        if status != 200:
            break
        batch = _rows(body)
        if not batch:
            break
        out.extend(batch)
        if (isinstance(body, dict) and body.get("hasNextPage") is False) or len(batch) < 100:
            break
        page += 1
        if page > 20:
            break
    return out


def _first(d, *keys, default=None):
    for k in keys:
        if isinstance(d, dict) and d.get(k) not in (None, ""):
            return d[k]
    return default


def extract_definition(project):
    """Pull a campaign definition from a Workduo project, probing several endpoint
    shapes for competitors and prompts. Returns (definition, diagnostics)."""
    pid = str(project.get("id"))
    entity = project.get("entity") or {}
    eid = entity.get("id")
    name = _first(project, "name", default=pid)
    target = _first(entity, "url", "domain", "website", default="")
    loc = project.get("location") or {}
    region = _first(loc, "region", "country", default=_first(project, "defaultRegion", "region", default="Singapore"))
    alt = entity.get("alternativeNames") or entity.get("aliases") or []
    diag = {}

    # --- Competitors: try inline first, then candidate endpoints ---
    competitors = []
    inline_ents = project.get("entities") or project.get("competitors") or []
    for e in inline_ents:
        if not isinstance(e, dict):
            continue
        tag = (e.get("tag") or e.get("type") or "").lower()
        if e.get("id") == eid or tag in ("primary", "self", "brand"):
            continue
        competitors.append(e.get("name") or e.get("entityName") or "")
    if not competitors:
        for url in (f"{WD_CORE}/projects/{pid}/entities", f"{WD_CORE}/entities",
                    f"{WD_DATA}/entities", f"{WD_CORE}/projects/{pid}/competitors"):
            params = {"projectId": pid} if "projects/" not in url else None
            st, body = _wd_get(url, params)
            diag[url] = st
            if st == 200:
                for e in _rows(body):
                    if not isinstance(e, dict):
                        continue
                    if e.get("id") == eid:
                        continue
                    tag = (e.get("tag") or e.get("type") or "").lower()
                    if tag in ("primary", "self", "brand"):
                        continue
                    nm = e.get("name") or e.get("entityName") or ""
                    if nm:
                        competitors.append(nm)
                if competitors:
                    break

    # --- Prompts / tracked queries (Workduo: /core/v1/queries?projectId, text in `query`) ---
    prompts, queries = [], []
    for url in (f"{WD_CORE}/queries", f"{WD_CORE}/projects/{pid}/queries", f"{WD_DATA}/queries"):
        params = {"projectId": pid, "pageSize": 200} if "projects/" not in url else {"pageSize": 200}
        st, body = _wd_get(url, params)
        diag[url] = st
        if st == 200:
            for p in _rows(body):
                if isinstance(p, str):
                    prompts.append(p)
                    queries.append({"text": p, "topic": "General", "region": region})
                elif isinstance(p, dict):
                    txt = _first(p, "query", "prompt", "text", "promptText", "name")
                    if txt:
                        prompts.append(txt)
                        queries.append({"text": txt,
                                        "topic": _first(p, "topic", "topicName", default="General"),
                                        "region": _first(p, "region", default=region)})
            if prompts:
                break

    # de-dup + cap
    seen, comp_clean = set(), []
    for c in competitors:
        c = (c or "").strip()
        if c and c.lower() not in seen:
            seen.add(c.lower())
            comp_clean.append(c)
    seenp, prompt_clean = set(), []
    for p in prompts:
        p = (p or "").strip()
        if p and p.lower() not in seenp:
            seenp.add(p.lower())
            prompt_clean.append(p)
    qmap = {}
    for q in queries:
        t = (q.get("text") or "").strip().lower()
        if t and t not in qmap:
            qmap[t] = q
    queries_clean = [qmap[p.lower()] for p in prompt_clean if p.lower() in qmap]

    # query text -> topic map (keyed by the exact prompt text the frontend renders).
    # This is what the GEO dashboard reads as `record.topics` to group queries by topic.
    topics = {}
    for p in prompt_clean[:30]:
        q = qmap.get(p.lower())
        if q:
            t = (q.get("topic") or "").strip()
            if t:
                topics[p] = t

    definition = {
        "id": pid,
        "source": "workduo",
        "name": name,
        "brand": _first(entity, "name", default=name),
        "target": target,
        "domain": domain_of(target),
        "region": region,
        "entityId": str(eid) if eid else "",
        "alternativeNames": [a for a in alt if a][:10],
        "competitors": comp_clean[:8],
        "prompts": prompt_clean[:30],
        "queries": queries_clean[:30],
        "topics": topics,
    }
    return definition, diag


