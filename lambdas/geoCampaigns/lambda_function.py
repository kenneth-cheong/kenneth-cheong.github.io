def _alert(text):
    """Surface a scheduled-run failure/summary to Google Chat (if GEO_ALERT_WEBHOOK is set) and
    always to CloudWatch. Never raises — alerting must not break the run."""
    try:
        print("[GEO-ALERT]", text)
        if ALERT_WEBHOOK:
            data = json.dumps({"text": text}).encode()
            req = urllib.request.Request(ALERT_WEBHOOK, data=data, method="POST",
                                         headers={"Content-Type": "application/json"})
            urllib.request.urlopen(req, timeout=10).read()
    except Exception as e:
        print("[GEO-ALERT] webhook failed:", str(e)[:160])


def _verify_one(prompt, brand, url, model, source, location, deadline=None, aliases=None):
    """Retry wrapper around one prompt×brand×platform check. Successful results (incl. a valid
    'not mentioned') return immediately; only failures are retried, with short backoff, and only
    when they look transient — so a flaky DataForSEO call self-heals instead of silently dropping
    the data point. Bounded to 3 attempts and never re-tries a permanent/bad-request error."""
    last = {"ok": False, "error": "no attempt"}
    for attempt in range(3):
        if deadline and time.time() > deadline:
            return {"ok": False, "error": "deadline exceeded"}
        res = _verify_once(prompt, brand, url, model, source, location, deadline, aliases)
        if res.get("ok"):
            return res
        last = res
        err = (res.get("error") or "").lower()
        if any(x in err for x in ("400", "invalid", "required", "unsupported", "deadline")):
            break  # permanent — retrying won't help
        if attempt < 2:
            time.sleep(min(2 ** attempt, 5))  # 1s, then 2s
    return last


def _verify_once(prompt, brand, url, model, source, location, deadline=None, aliases=None):
    """Python port of the JS WD.verifyOne — one prompt × brand × platform check.
    Returns the same dict shape the JS buildModel consumes. Never raises."""
    endpoint = DFS_ENDPOINT if source == "dataforseo" else AI_ENDPOINT
    try:
        _st, raw = _post_json(endpoint, {"action": "verify_mentions", "prompt": prompt,
                                         "brand": brand, "url": url, "location": location,
                                         "models": [model], "aliases": aliases or []}, timeout=90)
        data = _unwrap(raw)
        ver = data.get("verification") if isinstance(data, dict) else None
        result = ver[0] if ver else None
        if result and result.get("status") == "snapshot_pending":
            sid, tries = result.get("snapshot_id"), 0
            while tries < 30:
                if deadline and time.time() > deadline:
                    raise RuntimeError("deadline exceeded while polling snapshot")
                time.sleep(10)
                tries += 1
                _ps, praw = _post_json(AI_ENDPOINT, {"action": "poll_snapshot", "snapshot_id": sid,
                                                     "brand": brand, "url": url, "model": model}, timeout=60)
                pd = _unwrap(praw)
                status = pd.get("status") if isinstance(pd, dict) else None
                if status == "running":
                    continue
                if status == "success":
                    result = pd
                    break
                if isinstance(pd, dict) and pd.get("error"):
                    raise RuntimeError(pd["error"])
        if not result or result.get("status") == "error":
            raise RuntimeError((result or {}).get("error") or "no data")
        a = result.get("analysis")
        if isinstance(a, str):
            a = json.loads(a)
        a = a or {}
        cits = a.get("citation_urls") or []
        return {"ok": True, "mentioned": bool(a.get("is_mentioned")),
                "score": _num(a.get("visibility_score")), "sentiment": a.get("sentiment") or "neutral",
                "cited": bool(a.get("is_cited")), "rank": int(_num(a.get("rank"))),
                "snippet": a.get("mention_snippet") or "", "citations": cits[:12],
                # Evidence that grounds the sentiment for the user.
                "reason": (a.get("sentiment_reason") or "")[:200],
                "theme": (a.get("sentiment_theme") or "").strip()[:60],
                # Full text, untruncated — it goes to the S3 answer store, not DynamoDB, and is
                # stripped from the `results` blob before that write (see h_live_run_worker).
                "response": result.get("response") or ""}
    except Exception as e:
        return {"ok": False, "error": str(e)[:200]}


